# ── Indexes ──
ERROR_INDEX: Dict[str, Dict] = {e["error_code"]: e for e in ERROR_CATALOG}
CATEGORY_INDEX: Dict[str, List[Dict]] = {}
for _e in ERROR_CATALOG:
    CATEGORY_INDEX.setdefault(_e["category"], []).append(_e)
SEVERITY_INDEX: Dict[str, List[Dict]] = {}
for _e in ERROR_CATALOG:
    SEVERITY_INDEX.setdefault(_e["severity"], []).append(_e)


def get_error(error_code: str) -> Dict:
    """Get error definition by code. Returns empty dict if not found."""
    return ERROR_INDEX.get(error_code, {})


def get_errors_by_category(category: str) -> List[Dict]:
    """Get all errors in a category."""
    return CATEGORY_INDEX.get(category, [])


def get_errors_by_severity(severity: str) -> List[Dict]:
    """Get all errors at a given severity level."""
    return SEVERITY_INDEX.get(severity, [])


def get_score_impact(severity: str) -> int:
    """Get the quality score deduction for a severity level."""
    return SCORE_IMPACT.get(severity, 0)
